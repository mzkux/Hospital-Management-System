    // validate all user entry before sending new employee details to DB
    boolean isValidData() throws SQLException {
        clearErrorLabels();
        boolean result = true;
        
        String field_id = txtId.getText().trim();
        String field_name = txtName.getText().trim();
        String field_location = txtLocation.getText().trim();
        
        if (field_id.length() > 5) {
            lblErrorId.setText("Cannot exceed 5 chars");
            lblErrorId.setVisible(true);
            result = false;
        }
        
        if (!isInteger(field_id)) {
            lblErrorId.setText("Must be an integer");
            lblErrorId.setVisible(true);
            result = false;
        }
        
        if (field_id.isEmpty()) {
            lblErrorId.setText("Cannot be empty");
            lblErrorId.setVisible(true);
            result = false;
        }
        
        if (isDuplicate(field_id)) {
            lblErrorId.setText("Hospital already exists");
            lblErrorId.setVisible(true);
            result = false;
        }
        
        if (field_name.isEmpty()) {
            lblErrorName.setText("Cannot be empty");
            lblErrorName.setVisible(true);
            result = false;
        }
        
        if (field_name.length() > 30) {
            lblErrorName.setText("Cannot exceed 30 chars");
            lblErrorName.setVisible(true);
            result = false;
        }
        
        if (field_location.isEmpty()) {
            lblErrorLocation.setText("Cannot be empty");
            lblErrorLocation.setVisible(true);
            result = false;
        }
        
        if (field_location.length() > 30) {
            lblErrorLocation.setText("Cannot exceed 30 chars");
            lblErrorLocation.setVisible(true);
            result = false;
        }
        
        return result;
    }

    void clearInputBoxes() { // clear for every new entry 
        txtId.setText("");
        txtName.setText("");
        txtLocation.setText("");
    }

    private boolean isDuplicate(String id) throws SQLException {
        // check if hospital id already exists
        String stmtSQL = "SELECT id FROM hospital WHERE id = '" + id + "'";
        rs = dbCon.executeStatement(stmtSQL);
        return rs.isBeforeFirst();
    }
    private void btnAddNewHospitalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewHospitalActionPerformed
        // TODO add your handling code here:

        try {
            if (isValidData()) {
                String field_id = txtId.getText().trim();
                String field_name = txtName.getText().trim();
                String field_location = txtLocation.getText().trim();

                String prepSQL = "INSERT INTO hospital (id, name, location) VALUES ("
                        + "'" + field_id + "', "
                        + "'" + field_name + "', "
                        + "'" + field_location + "')";
                System.out.println("[INSERT Hospital] " + prepSQL);

                int result = dbCon.executePrepared(prepSQL);
                if (result > 0) {
                    javax.swing.JLabel label = new javax.swing.JLabel("New hospital added successfully.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                    clearInputBoxes();
                }

                rs.close();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error adding new hospital.");
        }
    }//GEN-LAST:event_btnAddNewHospitalActionPerformed

