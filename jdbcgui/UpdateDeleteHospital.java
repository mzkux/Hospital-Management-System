    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            
            
            // TODO: make sure no patients belong to this hospital
            ResultSet testPatients = dbCon.executeStatement("SELECT id FROM patient WHERE hospital_id = '" + rs.getString("id") + "'");
            if (testPatients.next()) {
                JOptionPane.showMessageDialog(null, "Cannot delete a hospital with patients assigned to it.");
                return;
            }
            testPatients.close();
            
            
            // TODO: make sure no employees belong to this hospital
            ResultSet testEmployees = dbCon.executeStatement("SELECT ssn FROM employeez WHERE hospital_id = '" + rs.getString("id") + "'");
            if (testEmployees.next()) {
                JOptionPane.showMessageDialog(null, "Cannot delete a hospital with employees assigned to it.");
                return;
            }
            testEmployees.close();
            
            
            String prepSQL = "DELETE hospital WHERE id = '" + txtId.getText().trim() + "'";
            System.out.println("[DELETE Hospital] " + prepSQL);
            
            // confirmation dialog
            if (JOptionPane.showConfirmDialog(null, "Are you sure you want to delete '" + txtName.getText().trim() + "'?") != 0) {
                JOptionPane.showMessageDialog(null, "Cancelled deletion.");
                return;
            } 
            
            int result = dbCon.executePrepared(prepSQL);
            if (result > 0) {
                javax.swing.JLabel label = new javax.swing.JLabel("Hospital of Id " + txtId.getText().trim() + " deleted successfully.");
                label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                getNewData();
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error deleting hospital.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

