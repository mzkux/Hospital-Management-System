    boolean isValidData() {
        clearErrorLabels();
        boolean result = true;
        
        String field_name = txtName.getText().trim();
        String field_location = txtLocation.getText().trim();
        
        if (field_name.isEmpty()) {
            lblErrorName.setText("Cannot be empty");
            lblErrorName.setVisible(true);
            result = false;
        }
        
        if (field_name.length() > 30) {
            lblErrorName.setText("Cannot exceed 30 chars");
            lblErrorName.setVisible(true);
            result = false;
        }
        
        if (field_location.isEmpty()) {
            lblErrorLocation.setText("Cannot be empty");
            lblErrorLocation.setVisible(true);
            result = false;
        }
        
        if (field_location.length() > 30) {
            lblErrorLocation.setText("Cannot exceed 30 chars");
            lblErrorLocation.setVisible(true);
            result = false;
        }
        
        return result;
    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        try {
            // make the result set scrolable forward/backward updatable

            if (isValidData()) {
                String field_id = txtId.getText().trim();
                String field_name = txtName.getText().trim();
                String field_location = txtLocation.getText().trim();
                
                String prepSQL = "UPDATE hospital SET name = " + 
                        "'" + field_name + 
                        "', location = '" + field_location + 
                        "' WHERE id = " + field_id;
                
                System.out.println("[UPDATE Hospital] " + prepSQL);
            
                // confirmation dialog
                if (JOptionPane.showConfirmDialog(null, "Are you sure you want to update this hospital?") != 0) {
                    JOptionPane.showMessageDialog(null, "Cancelled update.");
                    return;
                } 
                
                int result = dbCon.executePrepared(prepSQL);
                if (result > 0) {
                    javax.swing.JLabel label = new javax.swing.JLabel("Hospital of Id " + txtId.getText() + " updated successfully.");
                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);

                    getNewData();
                }
            }

        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error updating hospital." + e.getMessage());

        }
    }//GEN-LAST:event_btnUpdateActionPerformed


